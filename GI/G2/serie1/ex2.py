# ex2 série 1 op arithmitique

a = 7
b = 8

add = a + b

sous = a - b

mul = a * b

div = a / b

mod = a % b

exp = a ** b

floordiv = a // b



print(f"La somme de {a} et {b} est {add}")
print(f"La différence entre {a} et {b} est {sous}")
print(f"Le produit de {a} et {b} est {mul}")
print(f"Le quotient de {a} divisé par {b} est {div}")
print(f"Le reste de la division de {a} par {b} est {mod}")
print(f"{a} élevé à la puissance {b} est {exp}")
print(f"Le quotient entier de {a} divisé par {b} est {floordiv}")
