# Opérateur Arithmitique

a = 10
b = 4

# Somme
somme = a + b

print(f" la somme de {a} et {b} est {somme}")


# Soustraction
soustraction = a - b
print(f"La soustraction de {a} par {b} donne {soustraction}")

# Multiplication
multiplication = a * b
print(f"La multiplication de {a} par {b} donne {multiplication}")

# Division
division = a / b
print(f"La division de {a} par {b} donne {division}")

# Modulo
modulo = a % b
print(f"Le modulo de {a} par {b} donne {modulo}")

# Exponentiation
exponentiation = a ** b
print(f"L'exponentiation de {a} à la puissance {b} donne {exponentiation}")

# Division entière
division_entiere = a // b
print(f"La division entière de {a} par {b} donne {division_entiere}")